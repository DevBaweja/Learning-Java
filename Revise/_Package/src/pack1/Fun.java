package pack1;

public class Fun {

	private void pri_fun()
	{
		System.out.println("Private Mode");
	}
	
	public void pub_fun()
	{
		System.out.println("Public Mode");
	}
	
	protected void pro_fun()
	{
		System.out.println("Protected Mode");
	}
	
	 void fri_fun() // Default
	{
		System.out.println("Friendly Mode");
	}
}
